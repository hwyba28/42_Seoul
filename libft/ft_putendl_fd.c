#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	int		i;

	if (!s || fd < 0)
		return ;
	i = 0;
	while (s[i])
		write(fd, &s[i++], 1);
	write(fd, "\n", 1);
}
