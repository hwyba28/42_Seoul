void	ft_swap(int *a, int *b)
{
	int	tmp;

	tmp = *a;
	*a = *b;
	*b = tmp;
}

void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;

	i = 0;
	while (i < size)
	{
		j = i;
		while (j < size - i - 1)
		{
			if (tab[i] > tab[j])
				ft_swap(tab[i], tab[j]);
			j++;
		}
		i++;
	}
	return ;
}
